    static int so() {
        int d = 0;
        List<BanHang> list = new BanHang_DAO().MaDonDatHang();
        if (list.size() > 0) {
            String mahd = list.get(list.size() - 1).getMaHD();
            String so = mahd.substring(2);
            d = Integer.parseInt(so);
        } else {
            d = 0;
        }
        return d;
    }
    static int dem = so();

    static String changeMHD() {
        dem = dem + 1;
        String maHD = "HD";
        maHD += String.valueOf(dem);
        return maHD;
    }

